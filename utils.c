#include "so_long.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;

	str = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (i[s] != '\0')
	{
		i[str] = i[s];
		i++;
	}
	i[str] = '\0';
	return (str);
}
